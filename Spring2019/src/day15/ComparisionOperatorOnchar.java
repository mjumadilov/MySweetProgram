package day15;

public class ComparisionOperatorOnchar {

	public static void main(String[] args) {
		char c1 = 'a' ; 
		char c2 = 'B' ; 
		char c3 = ' ' ; 
		
		System.out.println(  (int) c1 );
		System.out.println(  (int) c2 );
		System.out.println(  (int) c3 );
		
		
		System.out.println(  c1==c2     );
		System.out.println(  c1!=c2     );
		
		System.out.println(  c1>=c2     );
		System.out.println(  c1<c2     );
		System.out.println(  c1<=c2     );
		
		

	}

}