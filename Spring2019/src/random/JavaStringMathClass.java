package random;

public class JavaStringMathClass {

	public static void main(String[] args) {

		double x = 28;
		double y = 4;
		
		System.out.println("Maximum number of x and y is :" + Math.max(x, y));
		System.out.println("Square root of y is :" + Math.sqrt(y));
		System.out.println("Power of x and y is :" + Math.pow(x, y));
		System.out.println("Logarith of x is :" + Math.log(x));
		System.out.println("Logarith of y is :" + Math.log(y));
		System.out.println("log10 of x is :" + Math.log10(x));
		System.out.println("log10 of y is :" + Math.log10(y));
		System.out.println("log1p of x is :" + Math.log1p(x));
		System.out.println("Maximum number of x is :" + Math.log1p(x));
		System.out.println("exp of x is :" + Math.exp(x));
		System.out.println("exp of x is :" + Math.expm1(x));

	}

}
