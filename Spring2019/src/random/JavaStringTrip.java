package random;

public class JavaStringTrip {

	public static void main(String[] args) {

		String s1 = " hello Aileen and Aisha   ";

		System.out.println(s1 + " my daughters");
		System.out.println(s1.length());

		System.out.println(s1.trim() + " my daughters!");

		String s2 = s1.trim();

		System.out.println(s2 + " my daughters!");
		System.out.println(s2.length());

	}

}
